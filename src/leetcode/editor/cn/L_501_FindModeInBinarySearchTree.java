/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public int[] findMode(TreeNode root) {
        if (root == null) return null;
        List<Integer> res = new ArrayList<>();
        int maxCount = 0;
        int count = 0;
        Deque<TreeNode> stack = new LinkedList<>();
        TreeNode cur = root;
        TreeNode pre = null;
        while (cur != null || !stack.isEmpty()) {
            if (cur != null) {
                stack.offerFirst(cur);
                cur = cur.left;
            } else {
                cur = stack.pollFirst();
                if (pre == null ) {  // ç¬¬ä¸€ä¸ªå…ƒç´ 
                    count = 1;
                } else if (cur.val == pre.val) {
                    count++;
                } else {
                    count = 1;
                }
                if (count == maxCount) {
                    res.add(cur.val);
                }
                if (count > maxCount) {
                    res.clear();
                    res.add(cur.val);
                    maxCount = count;
                }
                pre = cur;
                cur = cur.right;
            }
        }
        int[] result = new int[res.size()];
        for (int i = 0; i < res.size(); i++) {
            result[i] = res.get(i);
        }
        return result;
    }

}
